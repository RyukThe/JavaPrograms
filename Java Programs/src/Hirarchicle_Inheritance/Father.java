package Hirarchicle_Inheritance;

public class Father
{
	//ex hirarchicle inheritance
	//multiple subclasses aquire propery from 1 super class is called as hirarchicle inheritance
	public void Car()
	{
		System.out.println("Car:Audi");
	}
	public void Home()
	{
		System.out.println("Home:2BHK");
	}
	public void money()
	{
		System.out.println("money:10L");
	}
}
