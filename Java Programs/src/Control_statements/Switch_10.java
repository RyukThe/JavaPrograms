package Control_statements;

public class Switch_10 
{
	public static void main(String[] args) 
	{
		int inp=5;
		switch(inp)
		{
			case 1:System.out.println("red");
			break;
			
			case 2:System.out.println("yellow");
			break;
			
			case 3:System.out.println("white");
			break;
			
			case 4:System.out.println("black");
			break;
			
			case 5:System.out.println("orange");
			break;
			
			case 6:System.out.println("pink");
			break;
		}
	}
}
