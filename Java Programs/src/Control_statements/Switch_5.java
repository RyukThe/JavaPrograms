package Control_statements;

public class Switch_5 
{
	
	public static void main(String[] args) 
	{
		switch("Hi")
		{
		case "Hi":
			System.out.println("Hii......");
			break;
		case "bye":
			System.out.println("bye......");
			break;
			
		default:
			System.out.println("Invalid Input");
		}
	}
}
