package Java_Practice_Examples;

public class Veriables {
	
	public static void main(String[] args) {
		
		//1)Veriable declaration
		String StudentName;
		String DepartmentName;
		int rollno;
		char Grade;
		float percentage;
		
		//2)veriable initialization
		StudentName="Priyanka";
		DepartmentName="Computer";
		rollno=25;
		Grade='A';
		percentage=85.50f;
		
		
		//3)Variable usages
		
		System.out.println("Student Name= "+StudentName);
		System.out.println("Name: "+StudentName);    //reuse of code or info reuse
		System.out.println("Department Name= "+DepartmentName);
		System.out.println("Roll No= "+rollno);
		System.out.println("Percentage= "+percentage+"%");
		System.out.println("Grade= "+Grade);
	}
	
}
